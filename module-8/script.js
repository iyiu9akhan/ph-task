let firstPrice = 6;
let secondPrice = 3;
let thirdPrice = 6;

// double & triple equal start
console.log("(1) using == :", firstPrice == secondPrice);
console.log("(2) using == :", firstPrice == thirdPrice);

console.log("(3) using === :", firstPrice === secondPrice);
console.log("(4) using === :", firstPrice === thirdPrice);
// double & triple equal end

// Not Equal & Strict Not Equal start
console.log("(5) using != :", firstPrice != secondPrice);
console.log("(6) using != :", firstPrice != thirdPrice);

console.log("(7) using !== :", firstPrice !== secondPrice);
console.log("(8) using !== :", firstPrice !== thirdPrice);
// Not Equal & Strict Not Equal end

// less than & greater than
console.log("(9) using > : ", firstPrice > secondPrice);
console.log("(10) using > : ", firstPrice < secondPrice);
// less than & greater than

// less than or equal
console.log("(11) using <= :", firstPrice <= secondPrice);
console.log("(12) using >= :", firstPrice >= thirdPrice);
// greater than than or equal
